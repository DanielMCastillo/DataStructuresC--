const int coordx = 0;
const int coordy = 10;

void imprimir()
{
  for (int i = 0; i < FILAS; i++) {
    for (int j = 0; j < COLUMNAS; j++) {
      mvaddch(coordx +i, coordy+ j,tablero[i][j]);
  }
  }
}

int main()
{
    initscr();
    raw();
    keypad(stdscr,TRUE);
    noecho();

    imprimir();
    refresh();

    int ch;
    int cursorx = coordx+1;
    int cursory = coordy+1;
    move(cursorx,cursory);
    while ( (ch  = getch()) != 's' ) {
            switch (ch) {
            case KEY_RIGHT:
            cursory += 2;
            if ( cursory+2 > coordy + COLUMNAS ) {
                 cursory = coordy+1;
                 cursorx += 2;
            }
            if ( cursorx+2 > coordx + FILAS ) {
                cursorx = coordx+1;
            }

            move(cursorx,cursory);
            break;
            case KEY_LEFT:
            break;
            case '1': case '2': case '3':
            case '4': case '5': case '6':
            case '7': case '8': case '9':
            case ' ':
                 tablero[cursorx-coordx][cursory-coordy] = ch;

                 cursory += 2;
                if ( cursory+2 > coordy + COLUMNAS ) {
                 cursory = coordy+1;
                 cursorx += 2;
                }
                if ( cursorx+2 > coordx + FILAS ) {
                cursorx = coordx+1;
                }
                imprimir();
                move(cursorx,cursory);
                break;
                default:
                break;
            }

    }
    endwin();

    return 0;
}
